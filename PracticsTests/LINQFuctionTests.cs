using NUnit.Framework;
using Practics;
using System.Collections.Generic;

namespace PracticsTests
{
    [TestFixture]
    public class LINQFuctionTests
    {
        [SetUp]
        public void Setup()
        {
        }

        [Test]
        [TestCase(new[] { 1, 2, 3 }, new[] { 1, 2, 3 })]
        [TestCase(new[] { 1, 2, 2 }, new[] { 1, 2 })]
        public void GetUniqueMemberIds_CallWithUnique_Should(int[] mIds, int[] expectedMemberIds)
        {
            var members = new List<Member>();

            foreach (var id in mIds) members.Add(new Member { ID = id });

            var helper = new LINQFuction();

            var memberIds = helper.GetUniqueMemberIds(members);

            Assert.That(memberIds, Is.EqualTo(expectedMemberIds));
        }
    }
}